int Screen::KeyIn() {
	while (true) {
		int key = _getch(); // 콣 첎췇П
		if (key == 'M' || key == 'm') {
			return MAP;
		}
		else if (key == 224) {
			key = _getch();
			switch (key) {
			case UP:
				return UP;
				break;
			case DOWN:
				return DOWN;
				break;
			}
		}
		else if (key == 13) {
			return ENTER;
		}
	}
}

